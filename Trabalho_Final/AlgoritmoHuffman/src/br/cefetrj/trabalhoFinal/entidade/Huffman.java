package br.cefetrj.trabalhoFinal.entidade;

public class Huffman {
	
	private String palavra;
	private String simbolos;
	private String codigo;
	private String texto_compactado;
	private String texto_decodificado;
	private String repeticoes;
	public String getPalavra() {
		return palavra;
	}
	public void setPalavra(String palavra) {
		this.palavra = palavra;
	}
	public String getSimbolos() {
		return simbolos;
	}
	public void setSimbolos(String simbolos) {
		this.simbolos = simbolos;
	}
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getTexto_compactado() {
		return texto_compactado;
	}
	public void setTexto_compactado(String texto_compactado) {
		this.texto_compactado = texto_compactado;
	}
	public String getTexto_decodificado() {
		return texto_decodificado;
	}
	public void setTexto_decodificado(String texto_decodificado) {
		this.texto_decodificado = texto_decodificado;
	}
	public String getRepeticoes() {
		return repeticoes;
	}
	public void setRepeticoes(String repeticoes) {
		this.repeticoes = repeticoes;
	}
	
	

}
